# get numbers from user
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# round the numbers to the nearest integer
rounded_num1 = round(num1,2)
rounded_num2 = round(num2,2)

# print the rounded results
print(f"The rounded value of {num1} is: {rounded_num1}")
print(f"The rounded value of {num2} is: {rounded_num2}")
